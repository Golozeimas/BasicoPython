films_list = ["harry potter", "donnie darko", "homem aranha", "superman"]

# 1 - Buscar do ínicio até a segunda posição da listas de filmes
print(films_list[:2]) # lembre-se que é sempre o final com a contagem de array (começa no 0) - 1, para ser o que você quiser

# 2 - Buscar ultimo elemento da lista
print(films_list[-1])

# 3 - Buscar filmes até uma determinada posição
print(films_list[:3])

# 4 - Buscar filmes em uma posição em diante
print(films_list[1:3])

